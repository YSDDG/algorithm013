class Solution {
    public int coinChange(int[] coins, int amount) {
 /*
        1ã€é‡å¤å­é—®é¢˜ problem(i) = Min(sub_problem(i-1)...sub_problem(i-k))+1
        2ã€dpçŠ¶æ€ f(n) = Min{f(n - k) for k in [ 1, 2, 5]} + 1
        3ã€dpæ–¹ç¨‹ dp[i]=
         */

        int max = amount + 1;
        int[] dp = new int[max];
        Arrays.fill(dp, max);
        dp[0] = 0;
        for (int i = 1; i <= amount; i++) {
            for (int coin : coins) {
                if (coin <= i) {
                    dp[i] = Integer.min(dp[i], dp[i - coin] + 1);
                }
            }
        }
        return dp[amount] == max ? -1 : dp[amount];
    }
}
